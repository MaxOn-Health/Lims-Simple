interface SidebarProps {
  onNavigate?: () => void;
}

export const Sidebar: React.FC<SidebarProps> = ({ onNavigate }) => {
  const pathname = usePathname();

  const handleClick = () => {
    if (onNavigate) {
      onNavigate();
    }
  };

  const isActive = (href: string) => {
    if (href === '/dashboard') {
      return pathname === '/dashboard';
    }
    return pathname.startsWith(href);
  };

  return (
    <div className="flex-1 flex flex-col min-h-0 bg-white border-r border-gray-200">
      <div className="flex-1 flex flex-col pt-5 pb-4 overflow-y-auto">
        <div className="flex items-center flex-shrink-0 px-4">
          <h2 className="text-xl font-bold text-gray-900">LIMS</h2>
        </div>
        <nav className="mt-5 flex-1 px-2 space-y-1">
          {navigation.map((item) => {
            const active = isActive(item.href);

            if (item.allowedRoles) {
              return (
                <HasRole key={item.name} allowedRoles={item.allowedRoles}>
                  <Link
                    href={item.href}
                    onClick={handleClick}
                    className={`group flex items-center justify-between px-2 py-2 text-sm font-medium rounded-md ${active
                        ? 'bg-primary-50 text-primary-900'
                        : 'text-gray-700 hover:bg-gray-50 hover:text-gray-900'
                      }`}
                  >
                    <div className="flex items-center">
                      <span className="mr-3 flex-shrink-0">{item.icon}</span>
                      {item.name}
                    </div>
                    {item.badge && (
                      <span className="ml-2 inline-flex items-center px-2 py-0.5 rounded text-xs font-medium bg-primary-100 text-primary-800">
                        {item.badge}
                      </span>
                    )}
                  </Link>
                </HasRole>
              );
            }
            return (
              <Link
                key={item.name}
                href={item.href}
                onClick={handleClick}
                className={`group flex items-center justify-between px-2 py-2 text-sm font-medium rounded-md ${active
                    ? 'bg-primary-50 text-primary-900'
                    : 'text-gray-700 hover:bg-gray-50 hover:text-gray-900'
                  }`}
              >
                <div className="flex items-center">
                  <span className="mr-3 flex-shrink-0">{item.icon}</span>
                  {item.name}
                </div>
                {item.badge && (
                  <span className="ml-2 inline-flex items-center px-2 py-0.5 rounded text-xs font-medium bg-primary-100 text-primary-800">
                    {item.badge}
                  </span>
                )}
              </Link>
            );
          })}
        </nav>
      </div>
    </div>
  );
};

